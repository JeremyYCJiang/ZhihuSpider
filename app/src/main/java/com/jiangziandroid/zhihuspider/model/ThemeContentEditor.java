public class ThemeContentEditor {
    private String mEditorName;
    private String mEditorAvatarUrl;
    private int mEditorId;
    private String mEditorBio;
    private String mEditorUrl;

    public String getEditorName() {
        return mEditorName;
    }

    public void setEditorName(String editorName) {
        mEditorName = editorName;
    }

    public String getEditorAvatarUrl() {
        return mEditorAvatarUrl;
    }

    public void setEditorAvatarUrl(String editorAvatarUrl) {
        mEditorAvatarUrl = editorAvatarUrl;
    }

    public int getEditorId() {
        return mEditorId;
    }

    public void setEditorId(int editorId) {
        mEditorId = editorId;
    }

    public String getEditorBio() {
        return mEditorBio;
    }

    public void setEditorBio(String editorBio) {
        mEditorBio = editorBio;
    }

    public String getEditorUrl() {
        return mEditorUrl;
    }

    public void setEditorUrl(String editorUrl) {
        mEditorUrl = editorUrl;
    }
}
